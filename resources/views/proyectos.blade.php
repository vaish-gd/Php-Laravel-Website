			<b>Investigación:</b> Realizar investigación para comprender el impacto de las empresas y las organizaciones en la creación de sostenibilidad social y ambiental y valor económico.<br />
<b>Asociación:</b> Interactuar con los actores sociales para asumir los desafíos de la comunidad y sus organizaciones en materia de responsabilidad social y ambiental.<br />
<b>Diálogo:</b> Promover el diálogo y debate de todas las partes interesadas sobre cuestiones criticas relacionadas con sostenibilidad y responsabilidad social. Teniendo en cuenta estos principios, una posibilidad de implicar la responsabilidad social en la universidad es a través del Servicio Comunitario. El propósito es que esta actividad sea un punto de partida para la gestión y desarrollo de proyectos que vaya en mejora de la calidad de vida del ciudadano dentro de la universidad y en la sociedad donde este hace vida.Actualmente, el Centro Augusto Mijares tiene en marcha una Propuesta de Responsabilidad Social – Desarrollo Humano Sustentable en la Universidad de Oriente. La ejecución de este proyecto sería una oportunidad clave para llevar a la práctica el lema “Del pueblo venimos / hacia el pueblo vamos” que sirve de norte a esta Casa de Estudios Superiores.




