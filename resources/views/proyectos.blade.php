			Por el Centro AuguSto Mijares: Mgtr. Tamara Malaver
El Centro Agusto Mijares considera pertinente tratar el tema de la Responsabilidad Social- Desarrollo Humano Sustentable en el sistema educativo universitario, a partir de los aportes que la Organización de las Naciones Unidas (ONU) ha hecho sobre este asunto. En tal sentido, cree necesario centrar su acción teniendo en cuenta los Principios ONU Gestión Responsable de la Educación Universitaria en los cuales se expone lo siguiente:
<br />
<b>Propósito:</b> Desarrollar en los estudiantes las capacidades para la sostenibilidad, agregar valor a las empresas y la sociedad en general y trabajar por un economía incluyente y sostenible.
<br />
<b>Valores:</b> Incorporar en las actividades académicas y los programas, los valores de la responsabilidad social y el desarrollo sostenible.
<br />
<b>Métodos:</b> Crear Proyectos Educativos, materiales, procesos y entornos que permitan experiencias de aprendizaje eficaz para el liderazgo responsable

		</p>

		<p style="position: absolute;top:400px;right:50%;font-family: Roboto;
font-size: 15px;
font-weight: 400;
